import Foundation
import UIKit

extension UIFont{
    static func redHatBlack(ofSize fontSize: CGFloat) -> UIFont?{
        return UIFont(name: "RedHatDisplay-Black", size: fontSize)
    }
    
    static func redHatBlackItalic(ofSize fontSize: CGFloat) -> UIFont?{
        return UIFont(name: "RedHatDisplay-BlackItalic", size: fontSize)
    }
    
    static func redHatBold(ofSize fontSize: CGFloat) -> UIFont?{
        return UIFont(name: "RedHatDisplay-Bold", size: fontSize)
    }
    
    static func redHatBoldItalic(ofSize fontSize: CGFloat) -> UIFont?{
        return UIFont(name: "RedHatDisplay-BoldItalic", size: fontSize)
    }
    
    static func redHatItalic(ofSize fontSize: CGFloat) -> UIFont?{
        return UIFont(name: "RedHatDisplay-Italic", size: fontSize)
    }
    
    static func redHatMedium(ofSize fontSize: CGFloat) -> UIFont?{
        return UIFont(name: "RedHatDisplay-Medium", size: fontSize)
    }
    
    static func redHatMediumItalic(ofSize fontSize: CGFloat) -> UIFont?{
        return UIFont(name: "RedHatDisplay-MediumItalic", size: fontSize)
    }
    
    static func redHatRegular(ofSize fontSize: CGFloat) -> UIFont?{
        return UIFont(name: "RedHatDisplay-Regular", size: fontSize)
    }
}
